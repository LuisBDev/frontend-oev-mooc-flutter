class User {
  final int id;
  final String name;
  final String paternalSurname;
  final String maternalSurname;
  final String email;
  final String phone;
  final String role;

  User({
    required this.id,
    required this.name,
    required this.paternalSurname,
    required this.maternalSurname,
    required this.email,
    required this.phone,
    required this.role,
  });
}
